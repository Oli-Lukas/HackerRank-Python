#!/bin/python3

# Link: https://www.hackerrank.com/challenges/python-arithmetic-operators/problem

def main():
    a = int(input())
    b = int(input())

    summation = a + b
    difference = a - b
    product = a * b

    print(summation)
    print(difference)
    print(product)

if __name__ == '__main__':
    main()